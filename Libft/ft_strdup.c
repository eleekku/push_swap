#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	size_t	i;

	new = malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!new)
	{
		return (NULL);
	}
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
